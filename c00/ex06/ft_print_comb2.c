#include <unistd.h>

void	ft_display_arr(int j[])
{
	int	x;
	int	y;

	y = j[0] % 10 + '0';
	x = j[0] / 10 + '0';
	write (1, &x, 1);
	write (1, &y, 1);
	write (1, " ", 1);
	y = j[1] % 10 + '0';
	x = j[1] / 10 + '0';
	write (1, &x, 1);
	write (1, &y, 1);
	if (j[0] != 98)
		write (1, ", ", 2);
}

void	ft_print_comb2(void)
{
	int	i[2];

	i[0] = 0;
	while (i[0] <= 98)
	{
		i[1] = i[0] + 1;
		while (i[1] <= 99)
		{
			ft_display_arr(i);
			i[1]++;
		}
		i[0]++;
	}
}

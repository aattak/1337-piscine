int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	nb;

	i = 0;
	nb = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			nb = s1[i] - s2[i];
			return (nb);
		}
		i++;
	}
	return (nb);
}

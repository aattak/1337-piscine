unsigned int	ft_strlen(char *str)
{
	unsigned int	i;

	i = 0;
	while (*str)
	{
		str++;
		i++;
	}
	return (i);
}

unsigned int	ft_strlcat(char *dest, char *src, unsigned int size)
{
	unsigned int	i;
	unsigned int	j;
	unsigned int	ldst;
	unsigned int	lsrc;

	ldst = ft_strlen(dest);
	lsrc = ft_strlen(src);
	if (size <= ldst)
		return (lsrc + size);
	i = 0;
	j = ldst;
	while (src[i] != '\0' && j < size - 1)
	{
		dest[j] = src[i];
		i++;
		j++;
	}
	dest[j] = '\0';
	return (lsrc + ldst);
}
